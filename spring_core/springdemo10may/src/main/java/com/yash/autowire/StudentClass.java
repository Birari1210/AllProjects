package com.yash.autowire;

public class StudentClass {
  String classname;
  int roomno;
public StudentClass() {
	super();
}
public String getClassname() {
	return classname;
}
public void setClassname(String classname) {
	this.classname = classname;
}
public int getRoomno() {
	return roomno;
}
public void setRoomno(int roomno) {
	this.roomno = roomno;
}
  
}
