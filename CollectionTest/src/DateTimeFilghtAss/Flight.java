package DateTimeFilghtAss;

public class Flight {
	String flightname;
	String startingCity;
	String endingCity;
	double distance;
	double fare;
	
	public Flight(String flightname, String startingCity, String endingCity, double distance, double fare) {
		super();
		this.flightname = flightname;
		this.startingCity = startingCity;
		this.endingCity = endingCity;
		this.distance = distance;
		this.fare = fare;
	}

	

	

}
