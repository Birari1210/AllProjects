package jdbcAss;

public class Company {
	
	int compID;
	String comName;
	String comProduct;
	String productReleaseon;
	public int getCompID() {
		return compID;
	}
	public void setCompID(int compID) {
		this.compID = compID;
	}
	public String getComName() {
		return comName;
	}
	public void setComName(String comName) {
		this.comName = comName;
	}
	public String getComProduct() {
		return comProduct;
	}
	public void setComProduct(String comProduct) {
		this.comProduct = comProduct;
	}
	public String getProductReleaseon() {
		return productReleaseon;
	}
	public void setProductReleaseon(String productReleaseon) {
		this.productReleaseon = productReleaseon;
	}
		
}
